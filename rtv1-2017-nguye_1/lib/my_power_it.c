int	my_power_it(int nb, int power)
{
  int	nbr;

  nbr = nb;
  if (power == 0)
    return (1);
  else
    {
      while ((power - 1)> 0)
	{
	  nbr = nbr * nb;
	  power -= 1;
	}
      return (nbr);
    }
}
